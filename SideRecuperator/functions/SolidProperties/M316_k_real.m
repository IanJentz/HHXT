function val = M316_k_real(pos,time,temp)
    %316L thermal conductivity W/m-K
    T = [20;100;200;300;400;500;600;700;750];
    y = [14.1;15.4;16.8;18.3;19.7;21.2;22.6;23.9;24.6];
    
%     %Haynes 282 thermal conductivity W/m-K
%     T = [20;100;200;300;400;500;600;700;800;900;1000];
